CREATE DATABASE  IV
USE IV
GO
CREATE TABLE EMPLEADO
(
IDEMPLEADO INT PRIMARY KEY NOT NULL,
NOMBRE VARCHAR (30) NOT NULL,
APELLIDOS VARCHAR (30) NOT NULL,
FECHADENACIMIENTO DATE NOT NULL,
TELEFONO NVARCHAR (15) NOT NULL,
EMAIL NVARCHAR (100) NOT NULL,
SALARIO DECIMAL (10,2) NOT NULL
)
GO

CREATE TABLE CARGO
(
IDCARGO INT IDENTITY PRIMARY KEY NOT NULL,
DESCRIPCION VARCHAR (70)
)
GO

CREATE TABLE USUARIO
(
IDUSUARIO INT IDENTITY PRIMARY KEY NOT NULL,
NOMBRES VARCHAR (20),
APELLIDOS VARCHAR (20),
CONTRASEŃA NVARCHAR (10)
)
GO

CREATE TABLE CLIENTE 
(
IDCLIENTE INT IDENTITY PRIMARY KEY NOT NULL,
NOMBRE NVARCHAR (20),
APELLIDOS NVARCHAR (20),
DNI NUMERIC,
FECHA_REGISTRO DATE,
TELEFONO NUMERIC,
EMAIL NVARCHAR (50),
HISTORIAL_COMPRA VARCHAR (30)
)
GO

CREATE TABLE PRODUCTO 
(
IDPRODUCTO INT IDENTITY PRIMARY KEY NOT NULL,
NOMBRE_PRODUCTO VARCHAR(30),
MARCA VARCHAR(50),
DESCRIPCION VARCHAR(70),
ESTADO VARCHAR (40),
PRECIO_UNITARIO FLOAT
)
GO

CREATE TABLE PRODUCTO_DEFECTUOSO
(
IDPRODUCTO_DEFECTUOSO INT IDENTITY PRIMARY KEY NOT NULL,
FECHA_DETENCION VARCHAR (18),
DESCRIPCION VARCHAR (18),
FOREIGN KEY (IDPRODUCTO_DEFECTUOSO) REFERENCES PRODUCTO (IDPRODUCTO),

)
GO

CREATE TABLE CATEGORIA
(
IDCATEGORIA INT IDENTITY PRIMARY KEY NOT NULL,
NOMBRE_CATEGORIA VARCHAR(18),
DESCRIPCION VARCHAR (18),
FOREIGN KEY (IDCATEGORIA) REFERENCES PRODUCTO (IDPRODUCTO),
)
GO

CREATE TABLE PROVEEDOR
(
IDPROVEEDOR INT IDENTITY PRIMARY KEY NOT NULL,
NOMBRE_PROVEEDOR VARCHAR (20),
CONTACTO VARCHAR (20),
EMAIL NVARCHAR (50),
TELEFONO NUMERIC,
DIRECCION VARCHAR (60),
JIRON VARCHAR (30),
NUMERO INT
)
GO

CREATE TABLE ORDEN_COMPRA
(
IDORDEN_COMPRA INT IDENTITY PRIMARY KEY NOT NULL,
FECHA_PRODUCCION DATE,
FECHA_CADUCIDAD DATE,
FOREIGN KEY (IDORDEN_COMPRA) REFERENCES PROVEEDOR(IDPROVEEDOR),
FOREIGN KEY (IDORDEN_COMPRA) REFERENCES EMPLEADO (IDEMPLEADO),
)
GO
CREATE TABLE LOTE
(
IDLOTE INT IDENTITY PRIMARY KEY NOT NULL,
FECHA_PRODUCTO DATE,
TOTAL_ORDEN INT,
FOREIGN KEY (IDLOTE) REFERENCES PRODUCTO (IDPRODUCTO)
)
GO

CREATE TABLE PROMOCION
(
IDPROMOCION INT IDENTITY PRIMARY KEY NOT NULL,
CONDICION VARCHAR (40),
TIPO VARCHAR (40),
FECHA_INICIO DATE,
FECHA_FIN DATE
)
GO

CREATE TABLE DESCUENTO
(
IDDESCUENTO INT IDENTITY PRIMARY KEY NOT NULL,
NOMBRE VARCHAR (30),
TIPO VARCHAR (20),
PORCENTAJE VARCHAR (20),
FECHA_INICIO DATE,
FECHA_FIN DATE
FOREIGN KEY (IDDESCUENTO) REFERENCES PROMOCION(IDPROMOCION),
FOREIGN KEY (IDDESCUENTO) REFERENCES PRODUCTO (IDPRODUCTO),
)
GO

CREATE TABLE VENTA
(
IDVENTA INT IDENTITY PRIMARY KEY NOT NULL,
FECHA_VENTA DATE,
METODO_PAGO VARCHAR (20),
TOTAL FLOAT,
DESCUENTO DECIMAL (19,4)
)
GO

CREATE TABLE DETALLE_VENTA
(
IDDETALLE_VENTA INT IDENTITY PRIMARY KEY NOT NULL,
IDVENTA INT NOT NULL,
IDPRODUCTO INT NOT NULL,
CANTIDAD INT NOT NULL,
PRECIOUNITARIO DECIMAL (19,4) NOT NULL,
SUBTOTAL AS (CANTIDAD*PRECIOUNITARIO)
)
GO

ALTER TABLE DETALLE_VENTA
ADD 
  
    FOREIGN KEY (IDPRODUCTO) REFERENCES PRODUCTO (IDPRODUCTO),
    FOREIGN KEY (IDVENTA) REFERENCES VENTA (IDVENTA);
GO

CREATE TABLE DEVOLUCION
(
IDDEVOLUCION INT IDENTITY PRIMARY KEY NOT NULL,
MOTIVO VARCHAR (18),
FECHA DATE ,
FOREIGN KEY (IDDEVOLUCION) REFERENCES PRODUCTO (IDPRODUCTO),
FOREIGN KEY (IDDEVOLUCION) REFERENCES CLIENTE (IDCLIENTE),
FOREIGN KEY (IDDEVOLUCION) REFERENCES EMPLEADO (IDEMPLEADO),
FOREIGN KEY (IDDEVOLUCION) REFERENCES VENTA (IDVENTA)
)
GO

CREATE TABLE INVENTARIO
(
IDINVENTARIO INT IDENTITY PRIMARY KEY NOT NULL,
ENTRADAS VARCHAR (40),
SALIDAS VARCHAR (40),
FECHA_ACTUALIZACIÓN DATE
)
GO

CREATE TABLE UBICACION 
(
IDUBICACION INT IDENTITY PRIMARY KEY NOT NULL,
SECCION NVARCHAR (30),
ESTANTE VARCHAR (20),
NIVEL INT
)
GO

CREATE TABLE TRANSPORTE
(
IDTRANSPORTE INT IDENTITY PRIMARY KEY NOT NULL,
TIPO VARCHAR (18),
PLACA VARCHAR (18),
CAPACIDAD VARCHAR (18)
)
GO

CREATE TABLE PEDIDO
(
IDPEDIDO INT IDENTITY PRIMARY KEY NOT NULL,
DIRECCION VARCHAR (30),
FECHA_PEDIDO DATE,
FECHA_ENTREGA DATE,
FOREIGN KEY (IDPEDIDO) REFERENCES TRANSPORTE (IDTRANSPORTE),
)
GO

CREATE TABLE BOLETA_PEDIDO
(
IDBOLETA_PEDIDO INT IDENTITY PRIMARY KEY NOT NULL,
METODO_PAGO VARCHAR (30),
MONTO DECIMAL (19,4)
)
GO

CREATE TABLE CONDUCTOR 
(
IDCONDUCTOR INT IDENTITY PRIMARY KEY NOT NULL,
NOMBRE VARCHAR (18),
TELEFONO VARCHAR (18),
LICENCIA VARCHAR (18),
EMAIL VARCHAR (18)
)
GO

